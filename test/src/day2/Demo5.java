package day2;

public class Demo5 {

	public static void main(String[] args) {
		int a = 2;
		int b = 3;
		
		System.out.println("a:" + a);
		System.out.println("b:" + b);

		System.out.println("=======");

		++a;
		System.out.println("a:" + a);

		b++;
		System.out.println("b:" + b);
		System.out.println("========");

		System.out.println("a:"+a);
		--a;
		System.out.println("a:"+a);

	}

}
