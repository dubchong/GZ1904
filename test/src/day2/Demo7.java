package day2;
/**
 * 关系运算符的比较
 * @author dubai
 *
 */

public class Demo7 {
	public static void main(String[] args) {
		int x = 10;
		int y = 20;
		
		System.out.println(x==y);
		System.out.println(x!=y);
		System.out.println(x>y);
		System.out.println(x<y);
		System.out.println(x>=y);
		System.out.println(x<=y);
		
		System.out.println("======");
		
		int a = 1;
		int b = 2;
		int c = 3;
		
		System.out.println((a+b)==(b+c));
		
		
	}

}
